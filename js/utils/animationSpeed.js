/**
 * Модуль для расчета скорости анимации
 */

/**
 * Преобразует значение ползунка в скорость анимации
 * @param {number} value - значение ползунка (от 1 до 400)
 * @returns {number} - скорость анимации в миллисекундах
 */
export function getAnimationSpeed(value) {
    // Преобразуем диапазон 1-400 в скорость анимации
    // Значение 1 = самая медленная скорость (400 мс)
    // Значение 400 = самая быстрая скорость (1 мс)
    return Math.floor(401 - value);
} 